package quest.q626;

public class lcEmployee extends lcCompany {
    private String empName, deptPosition;
    private int price;

    public String getEmpName() { return empName; }
    public void setEmpName(String empName) { this.empName = empName; }

    public String getDeptPosition() { return deptPosition;}
    public void setDeptPosition(String deptPosition) { this.deptPosition = deptPosition;}

    public int getPrice() { return price; }
    public void setPrice(int price) { this.price = price; }
}
